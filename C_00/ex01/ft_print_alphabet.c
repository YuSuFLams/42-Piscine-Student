#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char	test;

	test = 'a';
	while (test <= 'z' )
	{
		ft_putchar(test);
		test++;
	}
}
